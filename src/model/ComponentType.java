package model;

/**
 * Enum representing different components that can be extracted or visualized for the image.
 */
public enum ComponentType {
  RED, //Represents the Red component of the image
  GREEN, //Represents the Green component of the image
  BLUE, //Represents the Blue component of the image
  VALUE, //Represents the Value component of the image
  INTENSITY, //Represents the Intensity component of the image
  LUMA //Represents the Luma component of the image
}
